// §8a — общее дерево талантов; §8b/§21 — деревья навыков 5 играбельных классов.
import type { StatMods } from './mods';

export interface SkillNode {
  id: string;
  name: string;
  desc: string;
  tier: number; // 1..6, порог по вложенным очкам ветки: (tier-1)*3
  mods: StatMods;
  key?: boolean; // *[ключ]* меняет поведение навыка (в срезе — маркер + бонус)
}

export interface SkillBranch {
  id: string;
  name: string;
  nodes: SkillNode[];
}

export interface SkillTree {
  branches: SkillBranch[];
}

// Тир открывается после (tier-1)*3 вложенных очков в ветку.
export function tierThreshold(tier: number): number {
  return (tier - 1) * 3;
}

