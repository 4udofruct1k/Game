// ---- §8b/§21 Классовые деревья (5 играбельных) ----
// Компактно: у каждого класса 3 ветки по 5 тиров, последний — *[ключ]*.
function branch(id: string, name: string, rows: [string, string, StatMods, boolean?][]): SkillBranch {
  return {
    id,
    name,
    nodes: rows.map((r, i) => ({
      id: `${id}_${i + 1}`,
      name: r[0],
      desc: r[1],
      tier: i + 1,
      mods: r[2],
      key: r[3] ?? false,
    })),
  };
}

